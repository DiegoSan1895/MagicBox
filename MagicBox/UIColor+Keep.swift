import UIKit

extension UIColor{
    
    class func keepNagigationBarColor() -> UIColor{
        return UIColor(red:0.16, green:0.14, blue:0.10, alpha:1)
    }
    
    class func keepRegisterBackgroundColor() -> UIColor{
        return UIColor(red:0.16, green:0.14, blue:0.16, alpha:1)
    }
    
    class func keepGetCodeButtonColor() -> UIColor{
        return UIColor(red:0.18, green:0.39, blue:0.31, alpha:1)
    }
}